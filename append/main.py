# Creamos una lista llamada 'lista_numeros' con cuatro elementos numéricos
lista_numeros = [12, 34, 56, 78]

# Usamos el método .append() para agregar un nuevo elemento a la lista.
# OJO: como estamos agregando una lista [79, 90, 100] y no números sueltos,
# esta lista se añade como UN SOLO elemento al final de 'lista_numeros'.
lista_numeros.append([79, 90, 100])

# Imprimimos la lista resultante
print(lista_numeros)

